        [HttpPost]
        [Authorize]
        public async Task<IActionResult> Post([FromBody] IEnumerable<VirtualVoucher> mat, [FromHeader] string mdhash, [FromHeader] int User)
        {
            if (await _mdHashValidator.ValidateMdHashAsync(mdhash, User))
            {
                try
            {
