        [HttpGet]
        [Authorize]
        public async Task<IActionResult> Get()
        {
            try
            {
                var products = await _projectSpecificationRepository.Get();
            return new OkObjectResult(products);
            }
            catch (Exception)
            { throw; }
        }
        [HttpPost("Getbyproject")]
        [Authorize]
        public async Task<IActionResult> Get([FromBody] SpecificationFilters specificationFilters, [FromHeader] string mdhash, [FromHeader] int User)
        {
            if (await _mdHashValidator.ValidateMdHashAsync(mdhash, User))
            {
                try
            {
                var product =await _projectSpecificationRepository.Getbyproject (specificationFilters);
            return new OkObjectResult(product);
            }
                catch (Exception ex)
                {
                    return StatusCode(500, new
                    {
                        message = $"An error occurred: {ex.Message}",
                        statusCode = 0
                    });
                }
            }
            else
            {
                return Unauthorized("Invalid MdHash");
            }
        }

