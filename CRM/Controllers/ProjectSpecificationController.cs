        [HttpPost]
        [Authorize]
        public async Task<IActionResult> Post([FromBody] IEnumerable<ProjectSpecificationMaster> projectSpecificationMasters, [FromHeader] string mdhash, [FromHeader] int User)
        {
            if (await _mdHashValidator.ValidateMdHashAsync(mdhash, User))
            {
                try
            {
                using (var scope = new TransactionScope(TransactionScopeAsyncFlowOption.Enabled))
            {
                    var val = await _projectSpecificationRepository.Insert(projectSpecificationMasters);

                scope.Complete();
                return new OkObjectResult(val);
                // return CreatedAtAction(nameof(Get), new { id = specificationMasters. }, specificationMasters);
            }
            }
                catch (Exception ex)
                {
                    return StatusCode(500, new
                    {
                        message = $"An error occurred: {ex.Message}",
